// 绘制8x8字符
void draw_char_8x8(uint32_t *framebuffer, int x, int y, char c, int color) {
    int char_index;
    
    // 处理扩展ASCII字符 (128-255)
    if (c >= 128 && c <= 255) {
        char_index = c;
    } else if (c >= 32 && c <= 126) {
        // 标准ASCII字符
        char_index = c - 32;
    } else {
        // 无效字符显示空格
        char_index = 0;
    }
    
    // 边界检查
    if (char_index < 0 || char_index >= 256) char_index = 0;
    
    const uint8_t *char_data = font_8x8_deepseek[char_index];
    
    for (int row = 0; row < 8; row++) {
        uint8_t row_data = char_data[row];
        for (int col = 0; col < 8; col++) {
            if (row_data & (0x01 << (7 - col))) {
                // 使用draw_pixel_swapped函数设置像素，自动进行边界检查
                draw_pixel_swapped(framebuffer, x + col, y + row, color);
            }
        }
    }
}

// 字符映射函数 - 将Unicode字符映射到扩展ASCII
unsigned char map_unicode_to_ascii(unsigned int unicode) {
    // 简单的映射表
    switch (unicode) {
        case 0x250C: return 0xC9; // ┌ 左上角
        case 0x2500: return 0xC4; // ─ 水平线
        case 0x2510: return 0xBB; // ┐ 右上角
        case 0x2502: return 0xBA; // │ 垂直线
        case 0x2514: return 0xC8; // └ 左下角
        case 0x2518: return 0xBC; // ┘ 右下角
        case 0x2593: return 0xB2; // ▓ 实心方块
        case 0x2588: return 0x80; // █ 实心方块
        default: return 0x20; // 空格
    }
}

// 绘制8x8字符串
void draw_string_8x8(uint32_t *framebuffer, int x, int y, const char *str, int color) {
    int char_x = x;
    while (*str) {
        // 检查是否是UTF-8字符
        if ((unsigned char)*str >= 0xC0) {
            // 这是一个UTF-8字符，需要解码
            unsigned int unicode = 0;
            if ((unsigned char)*str >= 0xE0) {
                // 3字节UTF-8
                unicode = ((unsigned char)str[0] & 0x0F) << 12 |
                         ((unsigned char)str[1] & 0x3F) << 6 |
                         ((unsigned char)str[2] & 0x3F);
                str += 3;
            } else {
                // 2字节UTF-8
                unicode = ((unsigned char)str[0] & 0x1F) << 6 |
                         ((unsigned char)str[1] & 0x3F);
                str += 2;
            }
            char mapped_char = map_unicode_to_ascii(unicode);
            draw_char_8x8(framebuffer, char_x, y, mapped_char, color);
        } else {
            // 标准ASCII字符
            draw_char_8x8(framebuffer, char_x, y, *str, color);
            str++;
        }
        char_x += 8;
    }
}

// 绘制6x8字符
void draw_char_6x8(uint32_t *framebuffer, int x, int y, char c, int color) {
    if (c < 0 || c > 127) c = 32; // 无效字符显示空格
    
    const char *char_data = font6x8_ascii[c];
    
    for (int row = 0; row < 8; row++) {
        char row_data = (row < 6) ? char_data[row] : 0x00; // 6x8字体，最后2行留空
        for (int col = 0; col < 6; col++) {
            if (row_data & (0x01 << (7 - col))) {
                // 使用draw_pixel_swapped函数设置像素，自动进行边界检查
                draw_pixel_swapped(framebuffer, x + col, y + row, color);
            }
        }
    }
}

// 绘制6x8字符串
void draw_string_6x8(uint32_t *framebuffer, int x, int y, const char *str, int color) {
    int char_x = x;
    while (*str) {
        draw_char_6x8(framebuffer, char_x, y, *str, color);
        char_x += 6;
        str++;
    }
}

